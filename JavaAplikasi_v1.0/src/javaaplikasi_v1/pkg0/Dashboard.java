    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        // TODO add your handling code here:
        String id_pasien = txtid2.getText();
        String nama_pasien = txtnama2.getText();
        String ruangan = (String) txtruang2.getSelectedItem();
        String jenis_kelamin = (String) gender1.getSelectedItem();
        String usia = txtusia2.getText();
        String tanggal_masuk = txtmasuk1.getText();
        String keluhan = txtkeluhan.getText();
        if (id_pasien.equals("")) { // proses pengecekan, apakah data ada yg kosong atau tidak
            JOptionPane.showMessageDialog(null, "Anda Belum Mengisi ID!", "Input Data Status", 0);
        } else if (nama_pasien.equals("")) {
            JOptionPane.showMessageDialog(null, "Anda Belum Mengisi Nama Dokter!", "Input Data Status", 0);
        } else if (ruangan.equals("")) {
            JOptionPane.showMessageDialog(null, "Anda Belum Mengisi Ruangan Inap!", "Input Data Status", 0);
        } else if (jenis_kelamin.equals("")) {
            JOptionPane.showMessageDialog(null, "Anda Belum Mengisi Jenis Kelamin!", "Input Data Status", 0);
        } else if (usia.equals("")) {
            JOptionPane.showMessageDialog(null, "Anda Belum Mengisi Usia!", "Input Data Status", 0);
        } else if (tanggal_masuk.equals("")) {
            JOptionPane.showMessageDialog(null, "Anda Belum Mengisi Tanggal Masuk!", "Input Data Status", 0);
        } else if (keluhan.equals("")) {
            JOptionPane.showMessageDialog(null, "Anda Belum Mengisi Keluhan!", "Input Data Status", 0);
        } else {
            try {
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/kuliah", "root", "");
                Statement st = conn.createStatement();
                String query = "INSERT INTO pasien VALUES(null,'" + id_pasien + "','" + nama_pasien + "','" + ruangan + "','" + jenis_kelamin + "','" + usia + "','" + tanggal_masuk + "','" + keluhan + "')";
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(null, "Data Berhasil DiSimpan");

                tampilkan_data_tabel_pasien();
                panel_tabel_pasien();
                reset_pasien();

            } catch (SQLException ex) {
                System.out.print("TERJADI KESALAHAN" + ex);
            }
        }
    }//GEN-LAST:event_jButton14ActionPerformed

