    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String id_dokter = txtid.getText();
        String nama_dokter = txtnama.getText();
        String bidang_dokter = (String) txtbidang.getSelectedItem();
        String ruang_praktik = (String) txtruang.getSelectedItem();
        String jadwal_praktik = (String) txtjadwal.getSelectedItem();
        String jam_praktik = txtjam.getText();
        if (id_dokter.equals("")) { // proses pengecekan, apakah data ada yg kosong atau tidak
            JOptionPane.showMessageDialog(null, "Anda Belum Mengisi ID!", "Input Data Status", 0);
        } else if (nama_dokter.equals("")) {
            JOptionPane.showMessageDialog(null, "Anda Belum Mengisi Nama Dokter!", "Input Data Status", 0);
        } else if (bidang_dokter.equals("")) {
            JOptionPane.showMessageDialog(null, "Anda Belum Mengisi Bidang Dokter!", "Input Data Status", 0);
        } else if (ruang_praktik.equals("")) {
            JOptionPane.showMessageDialog(null, "Anda Belum Mengisi Ruang Praktik!", "Input Data Status", 0);
        } else if (jadwal_praktik.equals("")) {
            JOptionPane.showMessageDialog(null, "Anda Belum Mengisi Jadwal Praktik!", "Input Data Status", 0);
        } else if (jam_praktik.equals("")) {
            JOptionPane.showMessageDialog(null, "Anda Belum Mengisi Jam Praktik!", "Input Data Status", 0);
        } else {
            try {
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/kuliah", "root", "");
                Statement st = conn.createStatement();
                String query = "INSERT INTO dokter VALUES(null,'" + id_dokter + "','" + nama_dokter + "','" + bidang_dokter + "','" + ruang_praktik + "','" + jadwal_praktik + "','" + jam_praktik + "')";
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(null, "Data Berhasil DiSimpan");

                tampilkan_data_tabel_dokter();
                panel_tabel_dokter();
                reset_dokter();

            } catch (SQLException ex) {
                System.out.print("TERJADI KESALAHAN" + ex);
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

